void main(List<String> args) {
  //Binary Infix operators
  const age=50;
  print("Addition: ${age+age}" );
  print("Subtraction: ${age-age}" );
  print("Multiplication: ${age*age}" );
  print("Division: ${age/age}" );
  print("Truncating : ${age~/age}");
  print("Remainder: ${age%age}");
  print("Equl: ${age==age}");
  print("Greater than: ${age>20}");
  print("Smaller than: ${age<20}");
  print("Greater than equal to : ${age>=20}");
  print("Smaller than equal to : ${age<=20}");


  //Bitwise infix operator
  print(age & 20);    //Bitwise AND Operator
  print(age | 20);    //Bitwise OR Operator
  print(age^20);  //Bitwise XOR operatorc
  print(age << 20);    //Bitwise left Operator
  print(age >> 20);    //Bitwise right Operator






}