void main(List<String> args) {
  const age1=62;
  const age2=30;
  // Addition
  print("Addition: ${age1+age2}");

  //Subtraction
  print("Subtraction: ${age1-age2}");

  //Multiplication
  print("Multiplication: ${age1*age2}");

  //Division
  print("Division: ${age1/age2}");

  //Truncating Division Operator
  // It converts the double to absolute integer
  const division=age1~/age2;
  print("Truncating Division: $division");

  


}